/**
 * Interfaz sonido, contiene las operaciones sin su implementación, estas tendrán que ser implementadas por una clase o componente
 * Visibilidad publica, podra ser vista desde la misma clase, sublase, paquete y para todos en general
 * @author cosmetorandell / rafelllull
 */
public interface Sonido {
	/**
	 * Método sonido
	 * Método abstracto de la interfaz sonido, es una declaración de la operación sin su implementación
	 * No retorna nada ni se le pasa nada por parámetro
	 */
	public abstract void sonido();

}
